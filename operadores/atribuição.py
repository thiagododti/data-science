#atribuição simples

saldo = 500 #r = 500 atribuido pelo =

#atribuição com adição
saldo = 500
saldo += 200 #r = 700

#atribuição com subtração
saldo = 500
saldo -= 100 #r = 400 

#atribuição com multiplicação
saldo = 500
saldo *= 2 #r = 1000

#atribuição com divisão
saldo = 500
saldo /= 5 #r = 100.0 - float

saldo = 500
saldo //= 5 #r = 100 - int

#atribuição com módulo
saldo = 500
saldo %= 480 #r = 20

#atribuição com exponenciação
saldo = 500
saldo **= 2 #r = 6400